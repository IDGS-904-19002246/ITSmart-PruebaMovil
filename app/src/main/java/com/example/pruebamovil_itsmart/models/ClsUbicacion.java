package com.example.pruebamovil_itsmart.models;

public class ClsUbicacion {

    public String estado;
    public String municipio;
    public String colonia;
    public String calle;

    public ClsUbicacion(String estado, String municipio, String colonia, String calle) {
        this.estado = estado;
        this.municipio = municipio;
        this.colonia = colonia;
        this.calle = calle;
    }
}
